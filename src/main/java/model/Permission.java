package model;

public class Permission {

    //Content//
    //----------------------------------------------------------------------------------------------------------------//
    private int id;
    private String description;


    //Getters & Setters//
    //----------------------------------------------------------------------------------------------------------------//
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
